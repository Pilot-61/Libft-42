#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t	srclen;
	size_t	dstlen;
	size_t	i;

	if (!dst && dstsize == 0)
		return (0);
	srclen = ft_strlen(src);
	dstlen = ft_strlen(dst);
	i = 0;
	if (dstsize < dstlen)
		return (srclen + dstsize);
	dstsize -= dstlen;
	if (dstsize)
	{
		while (src[i] && dstsize - 1 > 0)
		{
			dst[dstlen + i] = src[i];
			i++;
			dstsize--;
		}
		dst[dstlen + i] = '\0';
	}
	return (dstlen + srclen);
}
